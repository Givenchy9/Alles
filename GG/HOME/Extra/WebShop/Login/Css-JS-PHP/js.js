function myFunctionShowCart(){
    let z = document.getElementsByClassName("cb_sc");
    document.getElementById("cart").value = ("z = ", z);
    let total = 0;
    for(let i=0; i<z.length; i++) {
        document.getElementById("cart").value = ("i = " + i + ", " + z[i].value);
        if (z[i].checked) {
            total += 1*z[i].value;
        }
    }
    document.getElementById("cart").value = ("total = ", total);
}